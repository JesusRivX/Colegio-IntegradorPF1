    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            // Verificar si se ha seleccionado una fila para eliminar
            int filaSeleccionada = tblServicios.getSelectedRow();
            if (filaSeleccionada == -1) {
                JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para eliminar.");
                return;
            }

            // Confirmar si el usuario está seguro de eliminar la fila
            int opcion = JOptionPane.showConfirmDialog(null, "¿Está seguro de que desea eliminar esta fila?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (opcion == JOptionPane.YES_OPTION) {
                // Obtener el ID del servicio seleccionado
                int idServicio = Integer.parseInt(tblServicios.getValueAt(filaSeleccionada, 0).toString());

                // Preparar la consulta SQL para eliminar la fila de la base de datos
                PreparedStatement ps = cn.prepareStatement("DELETE FROM pago_servicios WHERE id_servicios=?");
                ps.setInt(1, idServicio);

                // Ejecutar la consulta para eliminar la fila
                int filasEliminadas = ps.executeUpdate();

                if (filasEliminadas > 0) {
                    JOptionPane.showMessageDialog(null, "Fila eliminada correctamente.");
                    mostrarDatos();
                    limpiarEntradas();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar la fila.");
                }
            }
        } catch (SQLException e) {
            System.out.println("Error al eliminar la fila: " + e);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

