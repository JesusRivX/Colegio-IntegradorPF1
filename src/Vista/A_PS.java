            ps.setDouble(3, sAdministrativo);

            double seguridad = Double.parseDouble(txtSeguridad.getText());
            ps.setDouble(4, seguridad);

            double actividad = Double.parseDouble(txtActividades.getText());
            ps.setDouble(5, actividad);

            String descripcion = txtDescripcion.getText();
            ps.setString(6, descripcion);

            // Calcular el monto total
            double total = sPublico + sEducativo + sAdministrativo + seguridad + actividad;
            ps.setDouble(7, total);

