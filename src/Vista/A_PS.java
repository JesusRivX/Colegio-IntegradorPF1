    private void mostrarDatos() {
        DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("N°Pago");
    modelo.addColumn("Servicios Pub.");
    modelo.addColumn("Servicios Educ.");
    modelo.addColumn("Servicios Admi.");
    modelo.addColumn("Seguridad");
    modelo.addColumn("Actividades");
    modelo.addColumn("Descripcion");
    modelo.addColumn("Mes");
    modelo.addColumn("Total");
    modelo.addColumn("Año");
    tblServicios.setModel(modelo);
    String consultasql = "SELECT * FROM pago_servicios";
    String data[] = new String[10]; // Cambio de longitud a 8

    Statement st;
    try {
        st = cn.createStatement();
        ResultSet rs = st.executeQuery(consultasql);
        double mayorGasto = 0;
        String mesMayorGasto = "";

        while (rs.next()) {
            data[0] = rs.getString(1);
            data[1] = rs.getString(2);
            data[2] = rs.getString(3);
            data[3] = rs.getString(4);
            data[4] = rs.getString(5);
            data[5] = rs.getString(6);
            data[6] = rs.getString(7);
            data[7] = rs.getString(8);
            data[8] = rs.getString(9);
            data[9] = rs.getString(10);

            // Obtener el total del registro actual
            double total = Double.parseDouble(data[8]);

            // Verificar si el total actual es mayor al mayor gasto registrado hasta ahora
            if (total > mayorGasto) {
                mayorGasto = total;
                mesMayorGasto = data[7]; // Actualizar el mes con el mayor gasto
            }

            modelo.addRow(data);
        }

        // Mostrar el mes con el mayor gasto y el total en los campos de texto correspondientes
        txtMesMayorGasto.setText(mesMayorGasto);
        txtTotalMayorGasto.setText(String.valueOf(mayorGasto));
    } catch (SQLException e) {
        System.out.println("Error al mostrar Datos " + e);
    }
    }

