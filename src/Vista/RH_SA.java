    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            // Obtener el valor del campo de texto a buscar
            String idABuscar = JOptionPane.showInputDialog("Ingrese el ID a buscar:");

            // Preparar la consulta SQL
            PreparedStatement ps = cn.prepareStatement("SELECT * FROM atencion WHERE id_atencion = ?");
            ps.setString(1, idABuscar);

            // Ejecutar la consulta y obtener los resultados
            ResultSet rs = ps.executeQuery();

            // Verificar si se encontró algún registro
            if (rs.next()) {
                // Obtener los valores de los campos y mostrarlos en un JOptionPane
                String id = rs.getString("id_atencion");
                String consultante = rs.getString("persona");
                String telefono = rs.getString("telefono");
                String fecha = rs.getString("fecha");
                String motivo = rs.getString("motivo");
                String descripcion = rs.getString("descripcion");
                String estado = rs.getString("estado");

                String mensaje = "ID: " + id + "\n" +
                                 "Consultante: " + consultante + "\n" +
                                 "Teléfono: " + telefono + "\n" +
                                 "Fecha: " + fecha + "\n" +
                                 "Motivo: " + motivo + "\n" +
                                 "Descripción: " + descripcion + "\n" +
                                 "Estado: " + estado;

                JOptionPane.showMessageDialog(null, mensaje);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró ningún registro con el ID proporcionado.");
            }
        } catch (SQLException e) {
            System.out.println("ERROR AL BUSCAR DATOS: " + e.getMessage());
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

