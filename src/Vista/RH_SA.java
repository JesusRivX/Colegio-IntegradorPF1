    private void tblAtencionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAtencionMouseClicked
        int fila = this.tblAtencion.getSelectedRow();
        this.txtID.setText(this.tblAtencion.getValueAt(fila, 0).toString());
        this.txtCon.setText(this.tblAtencion.getValueAt(fila, 1).toString());
        this.txtTelef.setText(this.tblAtencion.getValueAt(fila, 2).toString());

        // Convertir el String a Date para el campo de fecha
        try {
            SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
            Date fecha = formatoFecha.parse(this.tblAtencion.getValueAt(fila, 3).toString());
            this.txtFecha.setDate(fecha);
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

        // Llenar manualmente el JComboBox de Motivo
        String motivoSeleccionado = this.tblAtencion.getValueAt(fila, 4).toString();
        for (int i = 0; i < this.txtMotivo.getItemCount(); i++) {
            if (this.txtMotivo.getItemAt(i).equals(motivoSeleccionado)) {
                this.txtMotivo.setSelectedIndex(i);
                break;
            }
        }

        this.txtDescripcion.setText(this.tblAtencion.getValueAt(fila, 5).toString());

        // Llenar manualmente el JComboBox de Estado
        String estadoSeleccionado = this.tblAtencion.getValueAt(fila, 6).toString();
        for (int i = 0; i < this.txtEstado.getItemCount(); i++) {
            if (this.txtEstado.getItemAt(i).equals(estadoSeleccionado)) {
                this.txtEstado.setSelectedIndex(i);
                break;
            }
        }

    }//GEN-LAST:event_tblAtencionMouseClicked

    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        try {
            // Verificar si los campos obligatorios están vacíos
            if (txtCon.getText().isEmpty() ||
                txtTelef.getText().isEmpty() || txtFecha.getDate() == null || txtMotivo.getSelectedIndex() == 0 || 
                txtDescripcion.getText().isEmpty() || txtEstado.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos antes de agregar.");
                return; // Salir del método si algún campo obligatorio está vacío
            }
            
            PreparedStatement ps = cn.prepareStatement("INSERT INTO atencion (persona, telefono, fecha, motivo, descripcion, estado) VALUES (?,?,?,?,?,?)");
            
            ps.setString(1, txtCon.getText());
            ps.setString(2, txtTelef.getText());
            
            // Convertir la fecha de txtFecha al formato adecuado antes de establecerla en el PreparedStatement
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String fechaPago = dateFormat.format(txtFecha.getDate());
            ps.setString(3, fechaPago);
            
            ps.setString(4, txtMotivo.getSelectedItem().toString());
            ps.setString(5, txtDescripcion.getText());
            ps.setString(6, txtEstado.getSelectedItem().toString());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos GUARDADOS CORRECTAMENTE");
            mostrarDatos();
            limpiarEntradas();

        } catch(SQLException | NumberFormatException e) {
            System.out.println("ERROR AL REGISTRAR PROFESORES: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tblAtencion.getSelectedRow();

        if (filaSeleccionada == -1) {
            // Si no se ha seleccionado ninguna fila, mostrar un mensaje y salir del método
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para eliminar.");
            return;
        }

        // Obtener el ID del registro seleccionado
        String id = tblAtencion.getValueAt(filaSeleccionada, 0).toString();

        // Mostrar un mensaje de confirmación para asegurarse de que el usuario realmente quiere eliminar el registro
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Consulta realizada?");

        if (confirmacion == JOptionPane.YES_OPTION) {
            // Si el usuario confirma que quiere eliminar el registro, proceder con la eliminación
            try {
                // Crear la consulta SQL para eliminar el registro con el ID especificado
                String consultaSQL = "DELETE FROM atencion WHERE id_atencion= ?";

                // Preparar la sentencia SQL
                PreparedStatement ps = cn.prepareStatement(consultaSQL);
                ps.setString(1, id);

                // Ejecutar la sentencia SQL para eliminar el registro
                int resultado = ps.executeUpdate();

                // Verificar si se eliminó correctamente
                if (resultado > 0) {
                    JOptionPane.showMessageDialog(null, "Registro eliminado correctamente.");
                    mostrarDatos(); // Actualizar la tabla después de eliminar el registro
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar el registro.");
                }
                limpiarEntradas();
            } catch (SQLException ex) {
                System.out.println("Error al eliminar el registro: " + ex.getMessage());
            }
        } else {
            // Si el usuario cancela la eliminación, no hacer nada
            System.out.println("Eliminación cancelada.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            // Verificar si se ha seleccionado una fila en la tabla
            int filaSeleccionada = tblAtencion.getSelectedRow();
            if (filaSeleccionada == -1) {
                JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para actualizar.");
                return;
            }

            // Crear un formato de fecha
            SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");

            // Formatear la fecha en el formato adecuado
            String fechaFormateada = formatoFecha.format(txtFecha.getDate());

            // Construir la consulta SQL utilizando la fecha formateada
            PreparedStatement ps = cn.prepareStatement("UPDATE atencion SET persona=?, telefono=?, fecha=?, motivo=?, descripcion=?, estado=? WHERE id_atencion=?");
            ps.setString(1, txtCon.getText());
            ps.setString(2, txtTelef.getText());
            
            ps.setString(3, fechaFormateada); // Usar la fecha formateada aquí
            
            ps.setString(4, txtMotivo.getSelectedItem().toString());
            ps.setString(5, txtDescripcion.getText());
            ps.setString(6, txtEstado.getSelectedItem().toString());
            ps.setString(7, txtID.getText());

            // Ejecutar la consulta
            int indice = ps.executeUpdate();

            if (indice > 0) {
                JOptionPane.showMessageDialog(null, "DATOS ACTUALIZADOS CORRECTAMENTE");
                mostrarDatos();
                limpiarEntradas();
            } else {
                JOptionPane.showMessageDialog(null, "No seleccione Fila");
            }

        } catch (SQLException e) {
            System.out.println("ERROR AL ACTUALIZAR " + e);;
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

