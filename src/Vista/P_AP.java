    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        try {
            
            // Verificar si los campos obligatorios están vacíos
            if (txtNombre.getText().isEmpty() ||
                txtApellidos.getText().isEmpty() || nivel.getSelectedIndex() == 0 ||
                txtCorreo.getText().isEmpty() || curso1.getSelectedIndex() == 0 ||
                curso2.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos antes de agregar.");
                return; // Salir del método si algún campo obligatorio está vacío
            }
            
            
            PreparedStatement ps=cn.prepareStatement("INSERT INTO asignacion_cursos (id, nombres, apellidos, correo, curso1, curso2, nivel) VALUES (?,?,?,?,?,?,?)");
            ps.setString(1, txtID.getText());
            ps.setString(2, txtNombre.getText());
            ps.setString(3, txtApellidos.getText());
            ps.setString(4, txtCorreo.getText());
            ps.setString(5, curso1.getSelectedItem().toString());
            ps.setString(6, curso2.getSelectedItem().toString());
            ps.setString(7, nivel.getSelectedItem().toString());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos GUARDADOS CORRECTAMENTE");
            mostrarDatos();
            
            limpiarEntradas();
            
        } catch(SQLException e) {
            System.out.println("ERROR AL REGISTRAR PROFESORES" +e);
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

