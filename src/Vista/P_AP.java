    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // Crear un JDialog para la ventana de búsqueda
        JDialog dialogo = new JDialog((Frame) SwingUtilities.getWindowAncestor(this), "Ventana de Búsqueda", true);

        // Crear la tabla para mostrar los datos de registro de profesores
        JTable tabla = new JTable();
        JScrollPane scrollPane = new JScrollPane(tabla);
        dialogo.add(scrollPane);

        // Crear un modelo de tabla
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombres");
        modelo.addColumn("Apellidos");
        modelo.addColumn("Inicio");
        modelo.addColumn("Género");
        modelo.addColumn("Correo");
        modelo.addColumn("DNI");
        modelo.addColumn("Teléfono");
        tabla.setModel(modelo);

        // Llenar la tabla con los datos de la base de datos
        try {
            String consultaSql = "SELECT * FROM registro_profesores";
            Statement st = cn.createStatement();
            java.sql.ResultSet rs = st.executeQuery(consultaSql);
            while (rs.next()) {
                Object[] fila = new Object[8];
                for (int i = 0; i < 8; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modelo.addRow(fila);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar los datos de la base de datos: " + e.getMessage());
        }

        // Configurar el tamaño y la ubicación del JDialog
        dialogo.setSize(850, 460);
        dialogo.setLocationRelativeTo(this);
        
        // Agregar botones "Agregar" y "Salir"
        JButton btnAgregar = new JButton("Agregar");
        JButton btnSalir = new JButton("Salir");

        // Configurar ActionListener para el botón "Agregar"
        btnAgregar.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                // Obtener la fila seleccionada
            int filaSeleccionada = tabla.getSelectedRow();
            if (filaSeleccionada != -1) {
                // Obtener los datos de la fila seleccionada
                Object[] fila = new Object[modelo.getColumnCount()];
                for (int i = 0; i < fila.length; i++) {
                    fila[i] = modelo.getValueAt(filaSeleccionada, i);
                }
                // Mostrar los datos en los JTextField correspondientes del JPanel
                txtID.setText(fila[0].toString());
                txtNombre.setText(fila[1].toString());
                txtApellidos.setText(fila[2].toString());
                txtCorreo.setText(fila[5].toString());
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para agregar.");
            }
            }
        });

        // Configurar ActionListener para el botón "Salir"
        btnSalir.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                dialogo.dispose(); // Cerrar el JDialog
            }
        });

        // Crear un panel para contener los botones
        JPanel panelBotones = new JPanel();
        panelBotones.add(btnAgregar);
        panelBotones.add(btnSalir);

        // Agregar el panel de botones al JDialog
        dialogo.add(panelBotones, BorderLayout.SOUTH);

        dialogo.setVisible(true); // Mostrar el JDialog
        
    
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tblCursos.getSelectedRow();

        if (filaSeleccionada == -1) {
            // Si no se ha seleccionado ninguna fila, mostrar un mensaje y salir del método
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para eliminar.");
            return;
        }

        // Obtener el ID del registro seleccionado
        String id = tblCursos.getValueAt(filaSeleccionada, 0).toString();

        // Mostrar un mensaje de confirmación para asegurarse de que el usuario realmente quiere eliminar el registro
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro que desea eliminar este registro?");

        if (confirmacion == JOptionPane.YES_OPTION) {
            // Si el usuario confirma que quiere eliminar el registro, proceder con la eliminación
            try {
                // Crear la consulta SQL para eliminar el registro con el ID especificado
                String consultaSQL = "DELETE FROM asignacion_cursos WHERE id = ?";

                // Preparar la sentencia SQL
                PreparedStatement ps = cn.prepareStatement(consultaSQL);
                ps.setString(1, id);

                // Ejecutar la sentencia SQL para eliminar el registro
                int resultado = ps.executeUpdate();

                // Verificar si se eliminó correctamente
                if (resultado > 0) {
                    JOptionPane.showMessageDialog(null, "Registro eliminado correctamente.");
                    mostrarDatos(); // Actualizar la tabla después de eliminar el registro
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar el registro.");
                }
                limpiarEntradas();
            } catch (SQLException ex) {
                System.out.println("Error al eliminar el registro: " + ex.getMessage());
            }
        } else {
            // Si el usuario cancela la eliminación, no hacer nada
            System.out.println("Eliminación cancelada.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            
            // Verificar si se ha seleccionado una fila en la tabla
            int filaSeleccionada = tblCursos.getSelectedRow();
            if (filaSeleccionada == -1) {
                JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para actualizar.");
                return;
            }
            
            // Preparar la consulta SQL para actualizar solo los campos curso1, curso2 y nivel
            PreparedStatement ps = cn.prepareStatement("UPDATE asignacion_cursos SET curso1=?, curso2=?, nivel=? WHERE id=?");

            // Establecer los valores para los campos curso1, curso2 y nivel
            ps.setString(1, curso1.getSelectedItem().toString());
            ps.setString(2, curso2.getSelectedItem().toString());
            ps.setString(3, nivel.getSelectedItem().toString());

            // Establecer el ID como el criterio para identificar el registro a actualizar
            ps.setString(4, txtID.getText());

            // Ejecutar la consulta
            int indice = ps.executeUpdate();

            if (indice > 0) {
                JOptionPane.showMessageDialog(null, "Datos ACTUALIZADOS CORRECTAMENTE");
                mostrarDatos();
                limpiarEntradas();
            } else {
                JOptionPane.showMessageDialog(null, "No se seleccionó ninguna fila");
            }

        } catch (SQLException e) {
            System.out.println("ERROR AL ACTUALIZAR: " + e);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

