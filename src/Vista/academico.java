    private void jPanel12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel12MouseClicked
        JLabel label = new JLabel("Ingrese la contraseña:");
        JPasswordField passwordField = new JPasswordField();

        int result = JOptionPane.showConfirmDialog(null, new Object[]{label, passwordField}, "Contraseña", JOptionPane.OK_CANCEL_OPTION);

        if (result == JOptionPane.OK_OPTION) {
            String contraseñaIngresada = new String(passwordField.getPassword());
            if (verificarAcceso(contraseñaIngresada, true)) {
                A_ME p = new A_ME();
                p.setSize(1000, 670);
                p.setLocation(0, 0);

                content.removeAll();
                content.add(p, BorderLayout.CENTER);
                content.revalidate();
                content.repaint();
            } else {
                JOptionPane.showMessageDialog(null, "Acceso denegado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jPanel12MouseClicked

