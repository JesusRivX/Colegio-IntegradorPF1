    private boolean verificarAcceso(String contraseñaIngresada, boolean soloUsuario1) {
        try {
            String consulta = "SELECT contrasena FROM usuario WHERE id_usuario = 1";
            if (!soloUsuario1) {
                consulta = "SELECT contrasena FROM usuario";
            }

            PreparedStatement pst = cn.prepareStatement(consulta);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                String contraseñaUsuario = rs.getString("contrasena");
                if (contraseñaIngresada.equals(contraseñaUsuario)) {
                    return true;
                }
            }

            return false; // Contraseña incorrecta

        } catch (SQLException ex) {
            ex.printStackTrace();
            return false; // Error en la consulta
        }
    }
