    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            // Verificar si los campos obligatorios están vacíos
            if (txtNombres.getText().isEmpty() ||
                txtApellidos.getText().isEmpty() || txtFechNac.getDate() == null || txtGenero.getSelectedIndex() == 0 ||
                txtDNI.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtTelefono.getText().isEmpty() ||
                txtDireccion.getText().isEmpty() || txtFechCon.getDate() == null ||
                txtCargo.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos antes de agregar.");
                return; // Salir del método si algún campo obligatorio está vacío
            }

            // Preparar la consulta SQL
            String sql = "INSERT INTO registro_personal (nombres, apellidos, fech_nac, genero, dni, correo, telefono, direccion, fech_con, cargo) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = cn.prepareStatement(sql);

            // Obtener datos del formulario
            ps.setString(1, txtNombres.getText());
            ps.setString(2, txtApellidos.getText());
            ps.setDate(3, new java.sql.Date(txtFechNac.getDate().getTime())); // Suponiendo que txtFechNac es un JDateChooser
            ps.setString(4, txtGenero.getSelectedItem().toString());
            ps.setString(5, txtDNI.getText());
            ps.setString(6, txtCorreo.getText());
            ps.setString(7, txtTelefono.getText());
            ps.setString(8, txtDireccion.getText());
            ps.setDate(9, new java.sql.Date(txtFechCon.getDate().getTime())); // Suponiendo que txtFechCon es un JDateChooser
            ps.setString(10, txtCargo.getSelectedItem().toString());

            // Ejecutar la consulta
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Datos GUARDADOS CORRECTAMENTE");
            mostrarDatos();

            limpiarEntradas();

        } catch(SQLException | NumberFormatException e) {
            System.out.println("ERROR AL REGISTRAR EL PERSONAL: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void txtEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEliminarActionPerformed
        // Verificar si se ha seleccionado una fila en la tabla
        int filaSeleccionada = tblPersonal.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para eliminar.");
            return;
        }

        // Confirmar si el usuario está seguro de eliminar la fila
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar la fila seleccionada?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (confirmacion != JOptionPane.YES_OPTION) {
            return; // Cancelar la eliminación si el usuario selecciona "No"
        }

        try {
            // Obtener el ID del estudiante desde la fila seleccionada en la tabla
            int idPersonal = Integer.parseInt(tblPersonal.getValueAt(filaSeleccionada, 0).toString());

            // Preparar la consulta SQL para eliminar la fila de la base de datos
            PreparedStatement ps = cn.prepareStatement("DELETE FROM registro_personal WHERE id_personal=?");
            ps.setInt(1, idPersonal);

            // Ejecutar la consulta SQL para eliminar la fila
            int filasEliminadas = ps.executeUpdate();

            if (filasEliminadas > 0) {
                JOptionPane.showMessageDialog(null, "Fila eliminada correctamente.");
                mostrarDatos(); // Actualizar la tabla para reflejar los cambios
            } else {
                JOptionPane.showMessageDialog(null, "Error al eliminar la fila. Por favor, inténtelo de nuevo.");
            }

        } catch (SQLException e) {
            System.out.println("Error al eliminar la fila: " + e.getMessage());
        }
    }//GEN-LAST:event_txtEliminarActionPerformed

    private void txtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtActualizarActionPerformed
        try {
            // Verificar si se ha seleccionado una fila en la tabla
            int filaSeleccionada = tblPersonal.getSelectedRow();
            if (filaSeleccionada == -1) {
                JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para actualizar.");
                return;
            }

            // Crear un formato de fecha
            SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
            // Formatear la fecha en el formato adecuado
            String fechaFormateada = formatoFecha.format(txtFechNac.getDate());
            String fechaCon = formatoFecha.format(txtFechCon.getDate());

            // Preparar la consulta SQL para actualizar las notas, el promedio final y la beca
            PreparedStatement ps = cn.prepareStatement("UPDATE registro_personal SET nombres=?, apellidos=?, fech_nac=?, genero=?, dni=?, correo=?, telefono=?, direccion=?, fech_con=?, cargo=? WHERE id_personal=?");
            ps.setString(1, txtNombres.getText());
            ps.setString(2, txtApellidos.getText());
            ps.setString(3, fechaFormateada); // Usar la fecha formateada
            ps.setString(4, txtGenero.getSelectedItem().toString());
            ps.setString(5, txtDNI.getText());
            ps.setString(6, txtCorreo.getText());
            ps.setString(7, txtTelefono.getText());
            ps.setString(8, txtDireccion.getText());
            ps.setString(9, fechaCon);// Usar la fecha formateada
            ps.setString(10, txtCargo.getSelectedItem().toString());
            ps.setString(11, txtID.getText());

            // Ejecutar la consulta SQL para actualizar las notas, el promedio final y la beca
            int filasActualizadas = ps.executeUpdate();

            if (filasActualizadas > 0) {
                JOptionPane.showMessageDialog(null, "Registros actualizadas correctamente.");
                mostrarDatos(); // Actualizar la tabla para reflejar los cambios
                limpiarEntradas();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar los personales Por favor, inténtelo de nuevo.");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "ERROR CON LOS VALORES INGRESADOS");
        } catch (SQLException e) {
            System.out.println("Error al actualizar a los registrados: " + e.getMessage());
        }
    }//GEN-LAST:event_txtActualizarActionPerformed

