    private void registrarUsuario() {
        String usuario = txtUsuario.getText();
        String email = txtCorreo.getText();
        String contrase침a = new String(txtPassword.getPassword());
        String rol = "profesor"; 

        if (!email.endsWith("@gmail.com")) {
            JOptionPane.showMessageDialog(this, "El correo electr칩nico debe ser de Gmail", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            PreparedStatement ps=cn.prepareStatement("INSERT INTO usuario (usuario, correo, contrasena, rol) VALUES (?,?,?,?)");
            ps.setString(1, usuario);
            ps.setString(2, email);
            ps.setString(3, contrase침a);
            ps.setString(4, rol);
            int filasAfectadas = ps.executeUpdate();

            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(this, "Usuario registrado correctamente", "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error al registrar usuario", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            System.out.println("ERROR AL REGISTRAR PROFESORES" +e);
        }
    }
